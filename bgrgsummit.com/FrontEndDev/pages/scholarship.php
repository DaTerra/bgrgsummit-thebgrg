            <h3 class="titley">Guidelines for Completing the Scholarship Application Form</h3>
            
            <div class="">
              <p>Please complete this application for scholarship for the 2015 UCLA H3/ Black Gay Research Group Summit scheduled for January 14, 2015 in Atlanta, Georgia.</p>
              <p>All sections of the Scholarship Application Form must be completed in English.</p> 
              <p>Place “N/A” (not applicable) next to items that do not apply to you. Additional pages and other material will not be accepted. The Scholarship Application Form may be completed and submitted via e-mail as an attachment.</p>
              <p>Successful applicants will be offered scholarships based on individual need. The scholarships may include one or more of the following items: round-trip couch airfare, local transportation, lodging, and conference/summit registration.</p>
              <p>The information received in this application will be held in the strictest confidence and used only to evaluate the application for eligibility and representation. All personal information will be removed before the application is reviewed by the Scholarship Review Committee.</p>
              <p>The completed Scholarship Application Form must be sent electronically no later than September 30, 2014.</p>
              <p>Questions: Contact Craig Hutchinson, H3 Conference Chair at Craig@BGRGSummit.com</p>
