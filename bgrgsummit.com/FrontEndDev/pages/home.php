			<p>The BGRG was founded in 2001 in an effort to strengthen, empower, and provide a network for Black gay researchers and community workers/activists. Its mission is to advance an agenda of research, policy, and service in the interests of Black gay men.</p>
			<p>The aim for Health, Hope and Healing (H3) is to bring together local and national investigators who are conducting HIV research, junior investigators aspiring to work in this area, and community and faith-based members who provide services. The H3 conference will allow them to share experiences and findings and to develop collaborative networks for future studies and community programs. Each annual conference will focus on different biopsychosocial issues contributing to the HIV epidemic, address the challenges faced and report on progress towards decreasing HIV/AIDS among Black MSM and MSMW. </p>
			<h2>About BGRG</h2>
			<p>The Black Gay Research Group (BGRG) is a diverse group of Black gay men com- mitted to creating a platform for presenting, discussing, and analyzing scholarly work being produced by and on Black gay men. Inaugurated in the Spring of 2001, the BGRG has evolved from an informal discussion group to a structured group of professional Black gay men who create a sustainable research activity that contin- uously identifies, explores and promotes innovative program interventions within the Black gay community.</p>
			<h2>About UCLA Health, Hope and Healing (H3)</h2>
			<p>The mission of H3 which is to develop a national community collaborative to promote interdisciplinary research that examines and elucidates the behavioral, biological, psychosocial and cultural factors associated with HIV risks among Black men who have sex with men (MSM) and men who have sex with men and women (MSMW). This collaborative brings together the few local and national investigators who are conducting this research, as well as community and faith-based members who provide services to this population.</p>
