            <h3 class="titley">Emerging Scholar and Leadership</h3>                      
            <p>This award recognizes an outstanding Black gay man who has made a significant contribution to advancing the ideals of the Black Gay Research Agenda through scholarly research or other intellectual discourse or forms of scholarly activity. The BGRG Emerging Scholar and Leadership Award are given to a scholar who is early in their career as a researcher in public health, social science, art/literature, politics, and/or policy studies. This awardee demonstrates early exceptional contributions and promise in the development of knowledge relevant to lives of Black gay men. The recipient will also be someone who personally exhibits Black gay pride and through their work, demonstrates a commitment to advancing social justice for Black gay menâ€™s communities.</p>
            <p>Nominating Criteria and Instructions</p>
            <p>Nominees will be recommended by a BGRG member. The nominee must demonstrate: 1) Evidence of success and a consistent focus on the issues relevant to the lives of Black gay men, including, but not limited to HIV prevention; 2) Evidence of a record of scholarly publications, presentations, or other scholarly activities that generate and disseminate knowledge related to the lives of Black gay men. Nominees may be from a variety of disciplinary backgrounds and may have expertise in a variety of methods of inquiry. Nominees need not be active members of BGRG. Current BGRG Board of Director members or staff members of partnering administrative or sponsoring organizations are not eligible.</p>                        
